from abc import ABC, abstractmethod


# +----------------------------------------------------------------+
# |                          Interface                             |
# +----------------------------------------------------------------+

class Combatable(ABC):

    # +------------------------------------------------------------+
    # |                          MÃ©thodes                          |
    # +------------------------------------------------------------+

    @abstractmethod
    def attack(self, target) -> dict:
        pass

    @abstractmethod
    def defend(self, incoming_damage: int) -> dict:
        pass

    @abstractmethod
    def get_combat_stats(self) -> dict:
        pass
